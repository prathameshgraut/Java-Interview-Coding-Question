package Basic_Level;

public class Using_WhileLoop {

	//While Loop are Based On Condition & loop Process
	static void loop() {
		System.out.println("Print 1 To 10 Using While Loop,");
		int num=1;
		while(num<=10) {
			System.out.println(num);
			num++;
		}
	}
	
	public static void main(String[] args) {
		loop();
	}

}
